package tdc.edu.vn.nhom_10.model;

public class KiemKe {
    String ma;
    String ten;
    int nhap;
    int xuat;
    int ton;
    String thangnam;

    public KiemKe(){};

    public int getTon() {
        return ton;
    }

    public void setTon(int ton) {
        this.ton = ton;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getNhap() {
        return nhap;
    }

    public void setNhap(int nhap) {
        this.nhap = nhap;
    }

    public int getXuat() {
        return xuat;
    }

    public void setXuat(int xuat) {
        this.xuat = xuat;
    }

    public String getThangnam() {
        return thangnam;
    }

    public void setThangnam(String thangnam) {
        this.thangnam = thangnam;
    }
}
